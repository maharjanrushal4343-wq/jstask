//Check for Exclamation: Write an `if-else` statement to check if a string ends with an exclamation
//mark (!). Print "Exclamation mark" if true, otherwise "No exclamation mark."

let text = "hello!";

if (text.endsWith("!")) {
  console.log("Exclamation mark");
} else {
  console.log("No exclamation mark");
}
